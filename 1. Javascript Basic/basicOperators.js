/**
 * Basic Operators
 */


// Math Operators
var num1, num2

num1 = 75
num2 = 25

var sum = num1 + num2
console.log(sum); // 100

var subtract = num1 - num2
console.log(subtract); // 50

var multiplication = num1 * num2
console.log(multiplication); //  1875

var division = num1 / num2
console.log(division); // 3

var remainder = num1 % num2
console.log(remainder); // 0


// Logical Operator
var num1Bigger = num1 > num2
console.log(num1Bigger); // true

var num1Smaller = num1 < num2
console.log(num1Smaller); // false


// typeof Operator

var a

console.log(typeof 12); // number
console.log(typeof "Nayem"); // string
console.log(typeof num1Bigger); // boolean
console.log(typeof a); // undefined

 

 

